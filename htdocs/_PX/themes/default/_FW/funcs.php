	/**
	 * セットアップを検証する
	 */
	public function setup_test(){
		$errors = array();

		// システムディレクトリの確認
		$sysdirs = array(
			'_sys/' ,
			'_sys/applock/' ,
			'_sys/caches/' ,
			'_sys/publish/' ,
			'_sys/ramdata/' ,
		);
		foreach( $sysdirs as $sysdir ){
			$realpath = $this->px->dbh()->get_realpath( $this->px->get_conf('paths.px_dir').$sysdir );
			if( !is_dir( $realpath ) ){
				array_push( $errors, 'システムディレクトリ '.t::h($realpath).' が存在しません。' );
			}elseif( !is_writable( $realpath ) ){
				array_push( $errors, 'システムディレクトリ '.t::h($realpath).' に書き込み許可がありません。' );
			}
		}

		// 公開キャッシュディレクトリの確認
		$realpath = $this->px->dbh()->get_realpath( './'.$this->px->get_conf('system.public_cache_dir').'/' );
		if( !is_dir( $realpath ) ){
			array_push( $errors, '公開キャッシュディレクトリ '.t::h($realpath).' が存在しません。' );
		}elseif( !is_writable( $realpath ) ){
			array_push( $errors, '公開キャッシュディレクトリ '.t::h($realpath).' に書き込み許可がありません。' );
		}
		return $errors;
	}

	/**
	 * セットアップ検証結果を表示する
	 */
	public function mk_setup_test( $errors = array() ){
		// 結果のエラーメッセージ(または成功メッセージ)を生成して返す。
		$rtn = '';
		if( count($errors) ){
			$rtn .= '<h2>エラー</h2>';
			$rtn .= '<p>Pickles Framework のセットアップに、一部不備があります。<br />次の項目を確認してください。</p>';
			$rtn .= '<ul>';
			foreach( $errors as $error ){
				$rtn .= '<li>'.t::h($error).'</li>';
			}
			$rtn .= '</ul>';

		}else{
			$rtn .= '<p>おめでとうございます！<br />Pickles Framework のセットアップは正常に完了しました。</p>';
		}
		return $rtn;
	}

